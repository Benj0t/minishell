#include "libft.h"

int	ft_nbdigit16(unsigned int n)
{
	int		resu;

	if (n == 0)
		return (1);
	resu = 0;
	while (n != 0)
	{
		n = n / 16;
		resu++;
	}
	return (resu);
}
