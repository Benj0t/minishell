#include "libft.h"

void	ft_putnbr(int n)
{
	char c;

	if (n == -2147483648)
		ft_putstr_fd("-2147483648");
	else
	{
		if (n < 0)
		{
			ft_putchar_fd('-', 1);
			n = -n;
		}
		if (n / 10 > 0)
			ft_putnbr_fd(n / 10, 1);
		c = n % 10 + '0';
		ft_putchar_fd(c, 1);
	}
}
